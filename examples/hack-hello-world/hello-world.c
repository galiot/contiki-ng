    /*|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?-|-?--?-|-?-|-?-|-?-*/
    // εκτυπώνουμε το string που ορίσαμε παραπάνω.
    // αργότερα θα μπορούμε να το στείλουμε σαν json.
